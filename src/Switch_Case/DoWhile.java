package Switch_Case;

public class DoWhile {
    public static void main(String[] args) {
        int n=0;
        do {
            System.out.print(n + " ");
            n++;
        }
        while (n<=10);

        System.out.println();
        int i=10;
        while (i>0){
            System.out.print(i+" ");
            i--;
        }
    }
}
